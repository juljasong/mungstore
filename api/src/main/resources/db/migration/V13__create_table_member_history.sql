CREATE TABLE MEMBER_HISTORY (
    MEMBER_ID           BIGINT          NOT NULL,
    REVISION_ID         BIGINT          NOT NULL AUTO_INCREMENT,
    REVISION_TYPE       TINYINT(3)      NULL DEFAULT NULL,
    EMAIL               VARCHAR(50)     NOT NULL,
    PASSWORD            VARCHAR(100)    NOT NULL,
    NAME                VARCHAR(20)     NOT NULL,
    ROLE                VARCHAR(10)     NOT NULL,
    TEL                 VARCHAR(11),
    ZIPCODE             VARCHAR(5),
    CITY                VARCHAR(20),
    STREET              VARCHAR(50),
    LOGIN_FAIL_COUNT    INTEGER         NOT NULL,
    IS_LOCKED           BIT             NOT NULL,
    CREATED_BY          BIGINT,
    CREATED_AT          DATETIME(6)     NOT NULL,
    LAST_MODIFIED_BY    BIGINT,
    LAST_MODIFIED_AT    DATETIME(6)     NOT NULL,
    PRIMARY KEY (MEMBER_ID, REVISION_ID) USING BTREE,
    INDEX MEMBER_HISTORY_INDEX (REVISION_ID) USING BTREE,
    CONSTRAINT MEMBER_HISTORY_FK FOREIGN KEY (REVISION_ID) REFERENCES REVISION_HISTORY (REVISION_ID) ON UPDATE NO ACTION ON DELETE NO ACTION
);
